void FIT_ENDIAN_U16(unsigned short *value)
{
#ifdef LOGO_BIG_ENDIAN
  unsigned short orig = *value;
  char * buf = (char *)value;

  buf[0] = (orig) & 0xFF;
  buf[1] = (orig) >> 8L;
#endif
}

void FIT_ENDIAN_U32(unsigned long *value)
{
#ifdef LOGO_BIG_ENDIAN
  unsigned long orig = *value;
  char * buf = (char *)value;

  buf[0] = (char)(orig & 0xFF);
  buf[1] = (char)(orig >> 8L);
  buf[2] = (char)(orig >> 16);
  buf[3] = (char)(orig >> 24);
#endif
}

