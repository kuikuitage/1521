static s32 BmpReader(const u8* bmpaddr, const u32 size, BMP_HEADER* p_bmp_header, DIB_HEADER* p_dib_header)
{
  const u8* p = bmpaddr;

  if(*p != 'B' || *(p+1) != 'M')
    return -1;

  p += 2; //skip magic
  memcpy(p_bmp_header, p, sizeof(BMP_HEADER));
 
  FIT_ENDIAN_U32(&p_bmp_header->filesz);
  FIT_ENDIAN_U16(&p_bmp_header->reserve1);
  FIT_ENDIAN_U16(&p_bmp_header->reserve2);
  FIT_ENDIAN_U32(&p_bmp_header->bmp_offset);
 
  if(p_bmp_header->filesz != size)
    return -1;

  p += sizeof(BMP_HEADER);
  memcpy(p_dib_header, p, sizeof(DIB_HEADER));
  
  FIT_ENDIAN_U32(&p_dib_header->header_sz);
  FIT_ENDIAN_U32((u32*)&p_dib_header->width);
  FIT_ENDIAN_U32((u32*)&p_dib_header->height);
  FIT_ENDIAN_U16(&p_dib_header->nplanes);
  FIT_ENDIAN_U16(&p_dib_header->bitspp);
  FIT_ENDIAN_U32(&p_dib_header->compress_type);
  FIT_ENDIAN_U32(&p_dib_header->bmp_bytesz);
  FIT_ENDIAN_U32((u32*)&p_dib_header->hres);
  FIT_ENDIAN_U32((u32*)&p_dib_header->vres);
  FIT_ENDIAN_U32(&p_dib_header->ncolors);
  FIT_ENDIAN_U32(&p_dib_header->nimpcolors);
  
  return 0;
}


static s32 FillRegionBuffer(const u8* bmpaddr, const BMP_HEADER* p_bmp_header, const DIB_HEADER* p_dib_header, region_t * p_region)
{
  u8* p_bmp_data;
  s32 w, h;
  s32 width_with_padding;
  s32 i, j, m, n;
  u8* p_odd;
  u8* p_even;

  p_bmp_data = (u8*)bmpaddr + p_bmp_header->bmp_offset;
  w = p_dib_header->width;
  h = p_dib_header->height;
  width_with_padding = 4*((w + 3)/4);
  p_odd = (u8*)p_region->p_buf_odd;
  p_even = (u8*)p_region->p_buf_even;

  m = 0;
  n = 0;
  
  for( i = h-1; i >= 0; i-- ) 
  {
    if(0 == i%2)
    {
      for( j = 0; j < w; j++)
      {
        p_even[m++] = p_bmp_data[i*width_with_padding + j];
      }
    }
    else
    {
      for( j = 0; j < w; j++)
      {
        p_odd[n++] = p_bmp_data[i*width_with_padding + j];
      }
    }
  }

  return 0;
}


