void power_off_led_display(void)
{
  void *p_uio_dev = NULL;

  p_uio_dev = dev_find_identifier(NULL, DEV_IDT_TYPE, SYS_DEV_TYPE_UIO);
  MT_ASSERT(NULL != p_uio_dev);

  dev_io_ctrl(p_uio_dev, UIO_FP_SET_LOCK_LBD, 0);
}

void signal_led_display(u8 is_locked)
{
    void *p_uio_dev = NULL;
    p_uio_dev = dev_find_identifier(NULL, DEV_IDT_TYPE, SYS_DEV_TYPE_UIO);
    MT_ASSERT(NULL != p_uio_dev);
    if(is_locked)
    {
        dev_io_ctrl(p_uio_dev, UIO_FP_SET_LOCK_LBD, 1);
    }
    else
    {
        dev_io_ctrl(p_uio_dev, UIO_FP_SET_LOCK_LBD, 0);
    }
}

